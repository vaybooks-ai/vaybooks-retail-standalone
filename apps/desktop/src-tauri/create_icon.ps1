# Create a minimal valid ICO file
$icoData = @(
    0x00, 0x00, # Reserved
    0x01, 0x00, # Type (1 = ICO)
    0x01, 0x00, # Number of images
    0x01, 0x01, # Width (1 pixel)
    0x01, 0x01, # Height (1 pixel)
    0x00, 0x00, # Color count
    0x00,       # Reserved
    0x01, 0x00, # Color planes
    0x20, 0x00, # Bits per pixel
    0x30, 0x00, 0x00, 0x00, # Image data size
    0x16, 0x00, 0x00, 0x00, # Image data offset
    # Image data (1x1 pixel, 32-bit RGBA)
    0x00, 0x00, 0x00, 0xFF, # BGRA pixel
    0x00, 0x00, 0x00, 0x00, # Padding
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00
) -as [byte[]]

[System.IO.File]::WriteAllBytes('icons/icon.ico', $icoData)
Write-Host 'Icon file created successfully'
